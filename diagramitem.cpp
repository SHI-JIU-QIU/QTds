//! [0]
DiagramItem::DiagramItem(DiagramType diagramType, QMenu *contextMenu,
             QGraphicsItem *parent)
    : QGraphicsPolygonItem(parent)
{
    myDiagramType = diagramType;
    myContextMenu = contextMenu;

    QPainterPath path;
    switch (myDiagramType) {
        case StartEnd:
            path.moveTo(90, 0);
            path.arcTo(40, -60, 50, 50, 0, 90);
            path.arcTo(-80, -60, 50, 50, 90, 90);
            path.arcTo(-80, 0, 50, 50, 180, 90);
            path.arcTo(40, 0, 50, 50, 270, 90);
          //  path.lineTo(90, 0);
            myPolygon = path.toFillPolygon();
            break;
        case Conditional:
            myPolygon << QPointF(-100, 0) << QPointF(0, 100)
                      << QPointF(100, 0) << QPointF(0, -100)
                      << QPointF(-100, 0);
            break;
        case Step:
            myPolygon << QPointF(-100, -100) << QPointF(100, -100)
                      << QPointF(100, 100) << QPointF(-100, 100)
                      << QPointF(-100, -100);
            break;
        case Contact:
            path.moveTo(50, 0);
            path.arcTo(-50, -50, 100, 100, 0, 360);
            //path.lineTo(90, 0);
            myPolygon = path.toFillPolygon();
            break;
        case Documentation:
            path.moveTo(50, 0);
            path.arcTo(25, -50,50,50,90,0);
            path.arcTo(-75, -50,50,50,90,0);
            //path.arcTo(-50, 0,50,50,0,0);
            path.arcTo(-50, -25,50,50,180,180);
            path.arcTo(0, -25,50,50,0,180);
           // path.lineTo(50, 0);
            myPolygon = path.toFillPolygon();
            break;
        default:
            myPolygon << QPointF(-120, -80) << QPointF(-70, 80)
                      << QPointF(120, 80) << QPointF(70, -80)
                      << QPointF(-120, -80);
            break;
    }
    setPolygon(myPolygon);
    setFlag(QGraphicsItem::ItemIsMovable, true);
    setFlag(QGraphicsItem::ItemIsSelectable, true);
    setFlag(QGraphicsItem::ItemSendsGeometryChanges, true);
}
